fun main() {

    // readLine fará a leitura das informações
    val dado = readLine()

    // Conversão para tipos numéricos
    // *** ATENÇÃO *** Não é a melhor maneira de fazer.
    // PORÉM, como não queremos usar conceitos desconhecidos, seguiremos da maneira abaixo.
    dado.toString().toShort()
    dado.toString().toByte()
    dado.toString().toInt()
    dado.toString().toLong()
    dado.toString().toFloat()
    dado.toString().toDouble()
    dado.toString().toBoolean()
    
}