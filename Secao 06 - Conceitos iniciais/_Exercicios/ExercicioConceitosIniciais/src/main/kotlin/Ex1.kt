fun main() {
    // Declaração de variáveis para a base e altura do retângulo
    val base = 10.0   // Base do retângulo
    val altura = 5.0  // Altura do retângulo

    // Cálculo da área do retângulo usando a fórmula: (base * altura)
    val area = (base * altura)

    // Impressão do resultado
    println("A área do triângulo é: $area")
}