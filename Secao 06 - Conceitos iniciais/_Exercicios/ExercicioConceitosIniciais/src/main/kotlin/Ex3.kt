fun main() {
    // Soma de dois números
    val soma = 5 + 3
    println("Soma: $soma")

    // O valor da divisão de dois números
    val divisao = 10 / 2
    println("Divisão: $divisao")

    // O valor do resto da divisão de dois números
    val resto = 10 % 3
    println("Resto da divisão: $resto")

    // Incrementar e decrementar uma variável
    var numero = 10

    // Pré-incremento
    println("Pré-incremento: ${++numero}")

    // Pós-incremento
    println("Pós-incremento: ${numero++}")

    // Pré-decremento
    println("Pré-decremento: ${--numero}")

    // Pós-decremento
    println("Pós-decremento: ${numero--}")
}